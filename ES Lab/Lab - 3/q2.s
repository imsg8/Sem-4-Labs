	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors 
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =VALUE1
	LDR R1, [R0]
	LDR R2, =VALUE2
	LDR R3, [R1]
	UMULL R5, R6, R1, R3
	LDR R2, =RESULT
	STR R5, [R2], #4
	STR R6, [R2]
STOP
	B STOP
VALUE1 DCD 0x23
VALUE2 DCD 0x77
	AREA mydata, DATA, READWRITE
RESULT DCD 0
	END